    private void kolomUserKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_kolomUserKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        
        if(Character.isLetter(c)||Character.isWhitespace(c)||Character.isISOControl(c)){
            kolomUser.setEditable(true);
        }else{
            kolomUser.setEditable(false);
        }
    }//GEN-LAST:event_kolomUserKeyPressed


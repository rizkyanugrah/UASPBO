    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String Username = kolomUser.getText();
        String Password = kolomPass.getText();

        try {
            PC_view pc = new PC_view();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login_gui.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Login_gui.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            this.isLogin = Account.authentication(Username, Password);
        } catch (SQLException ex) {
            Logger.getLogger(Login_gui.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (this.isLogin) {
            JOptionPane.showMessageDialog(null, "Login Berhasil");
            this.dispose();
            try {
                new PC_view().setVisible(true);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Login_gui.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Login_gui.class.getName()).log(Level.SEVERE, null, ex);
            }
          
            
        }else{
            JOptionPane.showMessageDialog(null, "USERNAME ATAU PASSWORD SALAH");
            try {
                new Login_gui().setVisible(true);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Login_gui.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Login_gui.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
        }
        //        System.out.println(isLogin);
    }//GEN-LAST:event_jButton1MouseClicked

